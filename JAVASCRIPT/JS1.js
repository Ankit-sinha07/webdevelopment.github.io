//Arithmetic operator in action in JavaScript 
var n1 = 5
var n2 = 9
console.log("The value of n1 + n2 is-> " +(n1+n2)) //-> Addition
console.log("The value of n1 - n2 is-> " +(n1-n2)) //-> Subtraction
console.log("The value of n1 * n2 is-> " +(n1*n2)) //-> Multiplication
console.log("The value of n1 / n2 is-> " +(n1/n2)) //-> Division
console.log("The value of n1 ** n2 is-> " +(n1**n2)) //-> Exponential
console.log("The value of n1++  is-> " +(n1++)) //-> Increament-1
console.log("The value of ++n1 is-> " +(++n1)) //-> Increment-2
console.log("The value of n2-- is-> " +(n2--)) //-> Decrement -1
console.log("The value of n1 * n2 is-> " +(--n2)) //-> Decrement -1

